package co.com.jccp.ealgorithms;

public class DoubleIndex
{
    private double d;
    private int index;
    public DoubleIndex(double d, int index)
    {
        this.d = d;
        this.index = index;
    }

    public double getD() {
        return d;
    }

    public int getIndex() {
        return index;
    }
}
